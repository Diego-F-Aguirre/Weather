import Foundation
import UIKit


extension UIColor {
    
    static func darkPurple() -> UIColor {
        return UIColor(red:0.192, green:0.165, blue:0.220, alpha:1.00)
    }
    
    static func dirtyWhite() -> UIColor {
        return UIColor(red:0.980, green:0.949, blue:0.925, alpha:1.00)
    }
    
    static func darkGrey() -> UIColor {
        return UIColor(red:0.220, green:0.212, blue:0.208, alpha:1.00)
    }
    
}